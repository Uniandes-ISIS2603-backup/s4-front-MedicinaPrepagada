/**
* This class represents an farmacia of the Medisistemas app. 
* It contains all the information relevant to the farmacia.
*/
export class Farmacia {
    /**
    * The farmacia's id
    */
    id: number;

    /**
    * The farmacia's name
    */
    nombre: string;

    
    /**
    * The farmacia's ubicacion
    */
    ubicacion: string;
    
    /**
    * El telefono de la farmacia
    */
    telefono: number;
  
}




